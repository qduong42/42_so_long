#include "so_long.h"

size_t	ft_len(const char *s)
{
	size_t	i;

	i = 0;
	if (!s || s == NULL)
		return (1);
	while (s[i] != '\0' && s[i] != '\n')
	{
		i++;
	}
	return (i);
}
